
for (let i = 0; i < 101; i++) {
    if (i % 3 === 0 && i % 5 === 0) {

        // console.log("FizzBuzz");
        document.write("FizzBuzz" + "<br>");

    } else if (i % 3 === 0) {

        // console.log("Buzz");
        document.write("Buzz" + "<br>");

    } else if (i % 5 === 0) {

        // console.log("Fizz");
        document.write("Fizz" + "<br>");

    }

}